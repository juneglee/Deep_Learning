# 복합 대입 연산자 
# += : 숫자 덧셈 후 대입
# -= : 숫자 뺄셈 후 대입
# *= : 숫자 곱셈 후 대입
# /= : 숫자 나눗셈 후 대입
# %= : 숫자의 나머지를 구한 후 대입
# **= : 숫자 제곱 후 대입

#ex)
num = 100
num += 10
num -= 20
num *= 3
print("num 결과값 : ", num) # 270

# 문자열도 마찬가지로 += 와 *= 사용가능 

String = "안녕하세요" 
String += "!"
String += "!"
print("String 결과 : ", String) # 안녕하세요!!
# 원의 둘레와 넓이 구하기 
pi = 3.14159265
r = 10

print("원주율 = ", pi)
print("반지름 = ", r)
print("원의 둘레 = ", 2 * pi * r)
print("원의 넓이 = ", pi * r ** 2)

# 파이썬은 C, java 처럼 변수의 자료형에 대해 미리 선언하지 않고 
# 여러 종류의 자료형을 넣을 수 있는 유연성을 가지고 있다 
a = "문자열"

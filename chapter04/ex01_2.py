# 리스트 연선자 : 연결(+) , 반복(*), len()

# 리스트 연산자 
list_a = [1, 2, 3]
list_b = [4, 5, 6]

print("# 리스트")
print("list_a = ", list_a)
print("list_b = ", list_b)
print()

# 기본 연산자 :  연결(+) , 반복(*)
print("# 리스트 기본 연산자")
print("list_a + list_b =", list_a + list_b)
print("list_a * 3 =", list_a * 3)
print()

# 길이 구하기 : len() 
print("# 길이 구하기")
print("len(list_a) = ", len(list_a))

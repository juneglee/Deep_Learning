# 불 자료형

# 불 만들기 : 비교 연산자 
print(10 == 100) # False
print(10 != 100) # True
print(10 < 100) # True
print(10 > 100) # False
print(10 <= 100) # True
print(10 >= 100) # False

print("가방" == "하마") # True
print("가방" != "하마") # True
print("가방" < "하마") # True
 # 사전 순서로 가방이 하마보다 앞에 있으므로 작은 값을 갖는다.
print("가방" > "하마") # False

# 불 연산하기 : 논리 연산자 
# not and or 
# and : 모두 True 일 때만
print(True and True) #True 
print(True and False) #False
print(False and True) #False
print(False and False) #False
# or : 하나라도 True라면
print(True or True) #True 
print(True or False) #True
print(False or True) #True
print(False or False) #False

 
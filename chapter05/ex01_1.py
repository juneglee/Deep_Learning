# 함수의 기본 
#
# 함수를 사용하는 것을 함수를 호출한다 
# 함수를 호출할 때는 괄호 내부에 여러가지 자료를 넣게 되는데, 이러한 자료를 매개변수라 부른다 
# 마지막으로 함수를 호출해서 최종적으로 나오는 결과를 리턴값이라고 부른다 . 

# 사용법
# def 함수이름(): 
#     문장 

# 호출 : 함수를 실행하는 행위
# 매개 변수 : 함수의 괄호 내부에 넣는 것을 의미
# 리턴 값 : 함수의 최종적인 결과를 의미 
# 가변 매개변수 함수 : 매개변수를 원하는 만큼 받을 수 있는 함수 
# 기본 매개 변수 : 매개변수에 아무것도 넣지 않아도 들어가는 값

